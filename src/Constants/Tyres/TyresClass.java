package Constants.Tyres;

public class TyresClass {
    private String color;
    private String name;

    public TyresClass(String color, String name) {
        this.color = color;
        this.name = name;
    }

    // Getters
    public String getColor() {
        return color;
    }

    public String getName() {
        return name;
    }
}

